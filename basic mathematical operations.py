def Add(x, y):
    z=x+y
    print ('%d + %d = %d\n' %(x, y, z))

def Sub(x, y):
    z=x-y
    print ('%d - %d = %d\n' %(x, y, z))


def Mul(x, y):
    z=x*y
    print ('%d x %d = %d\n' %(x, y, z))


def Div(x, y):
    z=x/y
    print ('%d / %d = %d\n' %(x, y, z))

